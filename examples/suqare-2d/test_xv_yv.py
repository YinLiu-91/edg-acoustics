from collections import defaultdict

offset_dict = defaultdict(int)  # 记录每个坐标出现的次数
dx = 0.025  # x 方向偏移步长
dy = 0.025  # y 方向偏移步长

for i in range(len(x)):
    key = (x[i], y[i])
    cnt = offset_dict[key]  # 当前是该坐标的第 cnt 次出现
    offset_dict[key] += 1

    # 螺旋式偏移量
    off_x = (cnt % 2) * dx * (1 if cnt < 2 else -1)
    off_y = (cnt // 2) * dy * (1 if cnt % 4 < 2 else -1)

    plt.text(x[i] + off_x, y[i] + off_y, str(i), fontsize=12,color='red', ha='center', va='center')

