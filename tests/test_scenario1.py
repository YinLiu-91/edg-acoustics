def test_scenario1_simulation(simulation_parameters):
    """Test the acoustic simulation for scenario 1."""
    # Extract parameters
    params = simulation_parameters

    # Get paths
    current_dir = os.path.dirname(os.path.abspath(__file__))
    test_data_dir = os.path.join(current_dir, "./golden_files/test_scenario1/")

    # Load boundary conditions and parameters
    BC_para = []
    for material, label in params["BC_labels"].items():
        if material == "hard wall":
            BC_para.append({"label": label, "RI": 1})
        else:
            mat_files = glob.glob(f"{test_data_dir}/{material}*.mat")

            # Raise error if no .mat file found
            if not mat_files:
                pytest.fail(f"No .mat file found for material '{material}'")

            mat_file = scipy.io.loadmat(mat_files[0])

            material_dict = {"label": label}

            # Check if each variable exists in the .mat file and add it to the dictionary if it does
            if "RI" in mat_file:
                material_dict["RI"] = mat_file["RI"][0]
            else:
                material_dict["RI"] = 0

            if "AS" in mat_file and "lambdaS" in mat_file:
                material_dict["RP"] = numpy.array(
                    [mat_file["AS"][0], mat_file["lambdaS"][0]]
                )
            if (
                "BS" in mat_file
                and "CS" in mat_file
                and "alphaS" in mat_file
                and "betaS" in mat_file
            ):
                material_dict["CP"] = numpy.array(
                    [
                        mat_file["BS"][0],
                        mat_file["CS"][0],
                        mat_file["alphaS"][0],
                        mat_file["betaS"][0],
                    ]
                )

            BC_para.append(material_dict)
    BC_para += params["real_valued_impedance_boundary"]

    # Load mesh
    mesh_filename = os.path.join(test_data_dir, params["mesh_name"])
    mesh = edg_acoustics.Mesh(mesh_filename, params["BC_labels"])

    # Initialize and run simulation
    IC = edg_acoustics.Monopole_IC(params["monopole_xyz"], params["freq_upper_limit"])
    sim = edg_acoustics.AcousticsSimulation(
        params["rho0"], params["c0"], params["Nx"], mesh, params["BC_labels"]
    )

    flux = edg_acoustics.UpwindFlux(params["rho0"], params["c0"], sim.n_xyz)
    AbBC = edg_acoustics.AbsorbBC(sim.BCnode, BC_para)

    sim.init_BC(AbBC)
    sim.init_IC(IC)
    sim.init_Flux(flux)
    sim.init_rec(params["rec"], "scipy")  # locate the receiver points in the mesh

    tsi_time_integrator = edg_acoustics.TSI_TI(
        sim.RHS_operator, sim.dtscale, params["CFL"], Nt=3
    )
    sim.init_TimeIntegrator(tsi_time_integrator)

    # Run time integration
    sim.time_integration(
        total_time=params["impulse_length"],
    )

    # Post-processing
    results = edg_acoustics.Monopole_postprocessor(sim, 1)
